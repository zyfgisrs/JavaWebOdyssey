package com.zhouyf.demo2;

public class Person {
    private String name;

    private int age;

    private char sex;

    private String id;

    private String address;

    public Person(String name, int age, char sex, String id, String address) {
        this.name = name;
        this.age = age;
        this.sex = sex;
        this.id = id;
        this.address = address;
    }
}
